package Reveiw3;

public class Switch {
    public static void main(String[] args) {
        int wc=2022;

        switch (wc){

            case (2022):
                System.out.println("argentina");
                break;

            case(2018):
                    System.out.println("france");
                    break;

            case(2014):
                        System.out.println("germany");
                        break;
        }
    }
}
